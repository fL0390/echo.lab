function detectBrowserLang(): string {
    $accept = $_SERVER['HTTP_ACCEPT_LANGUAGE'] ?? '';
    $parts = preg_split('/,\s*/', $accept);
    foreach ($parts as $part) {
        $tag = strtolower(trim(explode(';', $part)[0]));
        if (str_starts_with($tag, 'es')) return 'es';
        if (str_starts_with($tag, 'en')) return 'en';
    }
    return 'en';
}

function getCurrentLang(): string {
    if (isset($_GET['lang']) && in_array($_GET['lang'], ['en', 'es'])) {
        $_SESSION['lang'] = $_GET['lang'];
        // Redirige a una URL limpia para que ?lang= desaparezca de la barra de direcciones
        $params = $_GET;
        unset($params['lang']);
        $base = strtok($_SERVER['REQUEST_URI'], '?');
        $qs   = count($params) ? '?' . http_build_query($params) : '';
        header('Location: ' . $base . $qs);
        exit;
    }
    // En la primera visita (no hay idioma en la sesión), detecta automáticamente el del navegador
    if (!isset($_SESSION['lang'])) {
        $_SESSION['lang'] = detectBrowserLang();
    }
    return $_SESSION['lang'];
}

function t(string $key, array $vars = []): string {
    global $TRANSLATIONS;
    $lang = getCurrentLang();
    $str = $TRANSLATIONS[$lang][$key] ?? $TRANSLATIONS['en'][$key] ?? $key;
    foreach ($vars as $k => $v) {
        $str = str_replace('{' . $k . '}', htmlspecialchars((string)$v), $str);
    }
    return $str;
}

